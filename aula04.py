# Tipos int e float
# int - Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. in sem sinal pe considerado positivo
print(11) # int
print(-11) # int
print(0) # int

# float - Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11) #float
print(-4.0, 0.0) #float

# A função type mostra o tipo que o Putho
# inferiu o valor
print(type('Otávio'))
print(type(1), type(-2), type(25))
print(type(1.0), type(-1.1), type(0.0))